'''
Tipos INT e Float
int-> Numero inteiro
O Tipo int representa qualquer numero positivo ou negativo, sem sinal é positivo
'''
print(11)
print(-11)
print(0)

# Float --> Numero com ponto flutuante
# o tipo float representa qualquer número
# positivo ouo negativo com ponto flutuante.
# Float sem sinal é considerado positivo
print(1.1, -11.5)

# A função type mostra o tipo que o python
# inferiu o valor
print(type('Geovani'))
print(type(1.1))
print(type(1), type (-1.1), type(0.0))