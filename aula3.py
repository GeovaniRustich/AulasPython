'''
Strings -> str -> Textos que estão dentro de aspas
'''

# Aspas Simples
print('Geovani Rustich')
print(1, 'Geovani "Rustich"')
# Aspas Duplas
print("Geovani Rustich")
print(2, "Geovani 'Rustich'")
# Escape -> '\' -> Ignorar aspas para abrir ou fechar a string -> não é recomendado a utilização desse recurso
print("Geovani \"Rustich\"")
# r
print(r"Geovani \"Rustich\"")